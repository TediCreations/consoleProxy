#include "console.h"

#include <stdio.h>

/******************************************************************************
	Definitions
******************************************************************************/

static char g_str[ 1000 ];

/******************************************************************************
	Local function declarations
******************************************************************************/

static char* getString( void )
{
	gets( g_str );
	return g_str;
}

void putString( const char* s )
{
	printf( s );
}

void putNum( const int num )
{
	printf( "%i", num );
}

static void console_init( console_t* obj )
{
	obj->gets = getString;
	obj->puts = putString;
	obj->putNum = putNum;
}

/******************************************************************************
	Function definitions
******************************************************************************/

console_t console_build( void )
{
	console_t aux;
	console_init( &aux );
	return aux;
}